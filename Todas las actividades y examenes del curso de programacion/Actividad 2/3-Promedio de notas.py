print("---CALCULO DE PROMEDIO---")
A = float(input("Ingresa tu primera nota: "))
B = float(input("Ingresa tu segunda nota: "))
C = float (input("Ingresa tu tercera nota: "))
promedio = (A + B + C) / 3
print(f"Tu promedio final es de: {promedio: 2f} puntos")
# El 2f sirve para que solo muestre 2 decimales