let diaSemana = 3;

switch (diaSemana) {
    case 1:
        console.log("Lunes: Lentejas");
        break;
    case 2:
        console.log("Martes: Pollo asado");
        break;
    case 3:
        console.log("Miercoles: Pescado frito");
        break;
    case 4:
        console.log("Jueves: Pasta");
        break;
    case 5:
        console.log("Viernes: arros");
        break;
    default:
        console.log("Dia no valido para menu.");
}